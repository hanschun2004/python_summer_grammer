# 32, 45, 48, 57, 84 중 가장 큰 값과 가장 작은 값을 구하세요
print(max(32, 45, 48, 57, 84))
print(min(32, 45, 48, 57, 84))

# 29, 95, 15, 85, 66 의 총합을 구하세요
print(sum([29, 95, 15, 85, 66]))

# 29, 95, 15, 85, 66 의 평균을 구하세요
print(sum([29, 95, 15, 85, 66]) / 5)

# 29, 95, 15, 85, 66 의 총합의 몫과 나머지를 구하세요
print(divmod(sum([29, 95, 15, 85, 66]), 5))

# 3,4,8,5 중 큰 값에서 작은값의 거듭제곱을 구하세요
print(pow(max(3,4,8,5), min(3,4,8,5)))


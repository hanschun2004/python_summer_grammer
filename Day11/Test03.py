# 튜플에서 사용되는 함수

tp = (1,1,2,2,2,3,3,3,3)

# .count(데이터) - 데이터의 개수를 구해준다.
print(tp.count(3))
print(tp.count(4))

# .index(데이터) - 데이터의 시작 인덱스를 알려준다.
print(tp.index(1))
print(tp.index(2))

#.__len__() - 데이터 개수
print(tp.__len__())
print(len(tp))

#.__str__() - 튜플 -> 문자열
print(tp.__str__())






















































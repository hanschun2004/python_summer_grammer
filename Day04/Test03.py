# 비교연산 - 결과값이 bool(논리형)으로 나온다. (True false)

n1, n2 = 10 , 3
print("10 > 3 :", n1 > n2) # 초과 , 크다
print("10 < 3 :", n1 < n2) # 미만, 작다
print("10 >= 3 :", n1 >= n2) # 이상, 크거나 같다
print("10 <= 3 :", n1 <= n2) # 이하, 작거나 같다
print("10 == 3 :", n1 == n2) # 같다, 일치한다
print("10 != 3 :", n1 != n2) # 다르다, 불일치한다

word1 = "apple"
word2 = "apple"
print(word1 == word2) # 값 비교

# not
# true -> false   false -> true
print(not n1 == n2)


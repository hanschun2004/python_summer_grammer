# 대입 연산 : 오른쪽의 값을 왼쪽 변수 공간에 복사하여 담는다.

n1 = 2
n2 = 4
n2 = n1 # 오른쪽 : 값
print(n1, n2)

# 대입 연산은 오른쪽의 식이 하나의 값으로 결정된 이후에 대입
answer = 2 *(3+4) / 5
print(answer)

name = '홍길동'
age = 28
form ="{}님은 {}살입니다."
result = form.format(name, age)
print(result)

# 복합 대입연산자 : 산술 연산자 + 대입 연산자
n1 = 10
n1 += 3
print(n1)

n2 = 2
n2 **= 5
print(n2)

